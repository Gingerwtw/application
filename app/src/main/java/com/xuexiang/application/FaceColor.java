

package com.xuexiang.application;

import java.io.Serializable;

public class FaceColor implements Serializable {

    private int black;
    private int yellow;
    private int light_yellow;
    private int grey;
    private int deep_red;
    private int red;

    public int getBlack() {
        return black;
    }
    public void setBlack(int black) {
        this.black = black;
    }
    public int getYellow() {
        return yellow;
    }
    public void setYellow(int yellow) {
        this.yellow = yellow;
    }
    public int getLight_yellow() {
        return light_yellow;
    }
    public void setLight_yellow(int light_yellow) {
        this.light_yellow = light_yellow;
    }
    public int getGrey() {
        return grey;
    }
    public void setGrey(int grey) {
        this.grey = grey;
    }
    public int getDeep_red() {
        return deep_red;
    }
    public void setDeep_red(int deep_red) {
        this.deep_red = deep_red;
    }
    public int getRed() {
        return red;
    }
    public void setRed(int red) {
        this.red = red;
    }

}
